    public void chay(){
        new Timer(20, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
               int value = progressBarLoad.getValue();
               
              if(value<progressBarLoad.getMaximum()){
               progressBarLoad.setValue(value + 1);
              } 
                else{
                  Chao.this.dispose();
              }
            }
        }).start();
    }

